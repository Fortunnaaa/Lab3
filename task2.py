n_str = input("Введіть шестизначне натуральне число: ")

# Перевірка, чи введено рядок з шістьма символами
if len(n_str) != 6 or not n_str.isdigit():
    print("Введено некоректне число. Введіть шестизначне натуральне число.")
else:
    n = int(n_str)  # Перетворення рядкової змінної в ціле число
    sum_of_digits = sum(map(int, str(n)))  # Знаходимо суму цифр числа
    print(f"Сума цифр числа {n} дорівнює {sum_of_digits}")
